            cbxKommentarer.addItem(kommentar);
            
            DataBasAcess.Connect();
            var kommentarQuery = "INSERT INTO KOMMENTAR (ID, NAMN) VALUES (" + String.valueOf(id) + "," + DataBasAcess.MakeDBString(kommentar) + ")";
             
