       public void fillMyTable() 
       {
        try {
            DataBasAcess.Connect();
    
            var idQuery = "SELECT * FROM INLAGGKATEGORI";
            var results = DataBasAcess.getidb().fetchRows(idQuery);
            var inlaggKategoriList = new ArrayList<InlaggKategori>();
             
        
            for (HashMap<String, String> result : results) {

                if (result != null) {
                    
                    var id = Integer.parseInt(result.get("ID"));
                    var name = result.get("INLAGG");
                    var kategori = result.get("KATEGORI");
                    
                    var ettKlartInlagg = new InlaggKategori(id, name, kategori);
                    
                    inlaggKategoriList.add(ettKlartInlagg);
         
                }
               
                for(int i = 0 ; i < inlaggKategoriList.size(); i++)
                {
                     tblInlagg.setValueAt(inlaggKategoriList.get(i).getId(), i, 0);
                     tblInlagg.setValueAt(inlaggKategoriList.get(i).getKategori(), i, 1);
                }
                
                
            }
        } catch (InfException ex) {
            Logger.getLogger(KommentarGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
       }

