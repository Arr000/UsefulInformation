        // TODO add your handling code here:
        var kunddb = new KundDB();
        
        var id = tfID.getText();
        var name = tfName.getText();
        var dateofbirth = tfDateOfBirth.getText();
        var phonenumber = tfPhoneNumber.getText();
        
        try {
           kunddb.addCustomer(Integer.parseInt(id), name, dateofbirth, phonenumber);
        } catch (ParseException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
